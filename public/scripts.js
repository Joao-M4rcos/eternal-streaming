$(document).ready(() => {
  let stream = 'all'
  const update = () => {
    const filter = $('#search').val()
    $('.cards').html('')
    const arr = data
      .filter(f => filter.length === 0 || new RegExp(`.*${filter}.*`, 'i').exec(f.title) || f.streams.find(f => new RegExp(`.*${filter}.*`, 'i').exec(f.key)))
      .filter(f => stream === 'all' || f.streams.find(f => f.key === stream) || f.streams.find(f => f.kid === stream) || f.streams.find(f => f.release === stream))
      
    if (arr.length === 0) $('.cards').append(`<h3>No movies found</h3>`)
    else {
      arr.forEach(i => {
        const card = $(`
        <div class="card">
          <div class="card__image-container">
          <a href="${i.id}" target="_blank"><img src="${i.image_url}" alt=""></a>
          </div>
          <div class="card__streams"></div>
        </div>
        `)
        i.streams.forEach(i => card.find('.card__streams').append(`<a href="${i.url}" target="_blank"><img src="${i.name}" alt=""></a>`))
        card.hide()
        $('.cards').append(card)
      })
      $('.card').fadeIn(1000)
    }
  }
  update()
  $('#search').on('keyup', () => { stream = 'all'; update() })
  $('.filters').on('click', function () { stream = $(this).data('value'); update() })
})

/*=== SLIDE CAROUSEL ===*/
var slideIndex = 0
var slides = document.getElementsByClassName("mySlides")

showSlides()

function showSlides() {
  var i;
  for (i = 0; i < slides.length; i++) {
    slides[i].style.display = "none"
  }
  slideIndex++;
  if (slideIndex > slides.length) { slideIndex = 1 }
  slides[slideIndex - 1].style.display = "block"
  setTimeout(showSlides, 8000);
}

function currentSlide(no) {
  var i;
  for (i = 0; i < slides.length; i++) {
    slides[i].style.display = "none"
  }
  slideIndex = no
  slides[no - 1].style.display = "block"
}

function plusSlides(n) {
  var newslideIndex = slideIndex + n
  if (newslideIndex < 4 && newslideIndex > 0) {
    currentSlide(newslideIndex)
  }
}
